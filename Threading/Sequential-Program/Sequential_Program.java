public class Sequential_Program {

    static void performTask1() {
        for (int i = 1; i <= 5; i++) {
            System.out.println("Task 1: " + i);
        }
    }

    static void performTask2() {
        for (int i = 1; i <= 5; i++) {
            System.out.println("Task 2: " + i);
        }
    }

    public static void main(String[] args) {
        performTask1();
        performTask2();
    }

}

